# Функция для приветствия (аналог void)
def greet():
    """Просто печатает приветствие, ничего не возвращает."""
    greetings = "Привет!"
    name = "Иржик"
    print(f"{greetings} {name}")

# Функция для вопроса о возрасте (аналог void)
def ask_and_recommend():
    """Спрашивает возраст и дает рекомендацию, ничего не возвращает."""
    age = int(input(f"Иржик, сколько тебе лет?\n"))

    if age == 5:
        print(f"Понятно, тебе {age} лет. Почитай Каштанку")
    elif age >= 6 and age <= 21:
        print(f"Понятно, тебе {age} лет. Почитай Курочку Рябу")
    else:
        print(f"Понятно, тебе {age} лет. Почитай Войну и Мир")

# Основная программа
def main():
    greet()  # Вызов функции приветствия
    ask_and_recommend()  # Вызов функции вопроса

# Запуск
main()